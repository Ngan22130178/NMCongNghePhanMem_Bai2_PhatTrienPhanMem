package vn.edu.nlu.fit.nmcnpm.my_silly_bestie.entity;

public class ToolEntity {
    private String toolId;
    private String toolName;
    private String cursorPath;
    private String sfxPath;

    // Getters và Setters
    public String getToolId() { return toolId; }
    public void setToolId(String toolId) { this.toolId = toolId; }

    public String getToolName() { return toolName; }
    public void setToolName(String toolName) { this.toolName = toolName; }

    public String getCursorPath() { return cursorPath; }
    public void setCursorPath(String cursorPath) { this.cursorPath = cursorPath; }

    public String getSfxPath() { return sfxPath; }
    public void setSfxPath(String sfxPath) { this.sfxPath = sfxPath; }
}